"""
    val = I_h1(k, x)

# Input
 - `k::Int`: frequency
 - `x::Float64`: point

# Output
 - `val::Float64`: value of the k-th basis function of the Sobolev space H1
"""
function I_h1(k::Int, x::Float64)::Float64
  return k == 0 ? 1.0 : sqrt(2)*cos(pi*k*x)
end
